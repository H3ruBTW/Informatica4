let main = document.getElementsByTagName("main");

function LightMode(){
    main[0].setAttribute('class', 'lightmode');
}

function LightModeCont(){
    main[0].setAttribute('class', 'lightmodecont');
}

function DarkMode(){
    main[0].setAttribute('class', 'darkmode');
}

function RedMode(){
    main[0].setAttribute('class', 'redmode');
}